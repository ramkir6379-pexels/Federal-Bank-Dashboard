# Boundary data now fetched from geo_processor
def get_filtered_locations(category, boundary_id=None):
    """
    Returns locations for a given category.
    If boundary_id (a pincode string) is provided, filters locations
    whose 'area' field matches the pincode.
    """
    locations = LOCATIONS.get(category, [])
    if boundary_id:
        locations = [loc for loc in locations if loc.get("area") == boundary_id]
    return locations


